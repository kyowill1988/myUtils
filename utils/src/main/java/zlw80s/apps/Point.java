package zlw80s.apps;

/**
 * 二维点
 *
 */
public class Point {
	
	private double x;//x坐标
	private double y;//y坐标
	
	public double getX() {
		return x;
	}
	
	public void setX(double x) {
		this.x = x;
	}
	
	public double getY() {
		return y;
	}
	
	public void setY(double y) {
		this.y = y;
	}
	
	public Point(double x,double y){
		
		this.x = x;
		this.y = y;		
	}
	
	
}
